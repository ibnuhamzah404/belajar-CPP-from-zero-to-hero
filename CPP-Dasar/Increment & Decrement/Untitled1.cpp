#include <iostream>

using namespace std;

int main ()
{
	//increment dan decrement
	//princrement dan postincrement
	
	int a = 5;
	int b = 5;
	
	//post increment
	cout << " \t\t== POST INCREMENT =="<<endl<<endl;
	cout << " Sebelum ditambahkan dengan a++ = ditambahkan dengan 1 "<<endl;
	cout << " "<<a << endl;
	cout << " Sesudah ditambah kan dengan a++ = ditambahkan dengan 1 "<<endl;
	cout << " "<<a++ << endl;
	cout << " "<<a << endl << endl;
	cout << " #Post Increment a++ maksudnya diprint dulu baru ditambahkan dengan 1"<<endl<<endl;
	
	//preincrement
	cout << "\t\t == PREINCREMENT =="<<endl<<endl;
	cout << " Sebelum ditambahkan dengan ++b = ditambahkan dengan 1 "<<endl;
	cout << " "<<b << endl;
	cout << " Sesudah ditambah kan dengan ++b = ditambahkan dengan 1 "<<endl;
	cout << " "<<++b << endl;
	cout << " "<<b << endl << endl;
	cout << " #Pre Increment b++ maksudnya ditambahkan dengan 1 dulu baru diprint ";

	
	
	
	
}
